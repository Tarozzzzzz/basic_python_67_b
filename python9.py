import math

x = min(5,10,15) #ค่าน้อยสุด
y = max(5,10,15) #ค่ามากสุด
print(x, y)

z = abs(-7.25) # |-7.25| = 7.25
print(z)

v = pow(4, 3) # 4x4x4
print(v)

pi = math.pi
print(pi)

s_root = math.sqrt(64)
print(s_root)

x = math.ceil(1.3) # ปัดตามการเงิน 2
y = math.floor(1.3) # ปัดตามวิทย์ ต่ำกว่า 5 ปัดลง มากกว่า ปัดขึ้น
print(x,y)